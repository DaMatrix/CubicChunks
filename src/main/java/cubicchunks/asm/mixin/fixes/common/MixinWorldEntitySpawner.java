package cubicchunks.asm.mixin.fixes.common;

import net.minecraft.entity.EnumCreatureType;
import net.minecraft.world.WorldEntitySpawner;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Redirect;

@Mixin(WorldEntitySpawner.class)
public abstract class MixinWorldEntitySpawner {
    @Redirect(method = "Lnet/minecraft/world/WorldEntitySpawner;findChunksForSpawning(Lnet/minecraft/world/WorldServer;ZZZ)I",
    at = @At(value = "INVOKE",
    target = "Lnet/minecraft/entity/EnumCreatureType;getMaxNumberOfCreature()I"))
    public int changeMaxCount(EnumCreatureType type) {
        return 5;
    }
}
